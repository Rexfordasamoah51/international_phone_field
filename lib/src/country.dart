class Country {
  final String name;
  final String uniCode;
  final String dial;
  final String currency;
  final String capital;
  final String continent;
  final String iso2;
  final String iso3;
  Country({this.uniCode, this.dial, this.currency, this.capital, this.continent,
      this.iso2, this.iso3,
      this.name});
}
